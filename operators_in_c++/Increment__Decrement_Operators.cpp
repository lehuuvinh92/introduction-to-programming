#include "stdafx.h"
#include <iostream> 
using namespace std; 
int main()
{
	int a = 10, b = 100;

    cout<<"++a = "<<++a<<endl;
	cout<<"a++ = "<<a++<<endl;
    cout<<"--b = "<<--b<<endl;
	cout<<"b-- = "<<b--<<endl;
	system("pause");
}
